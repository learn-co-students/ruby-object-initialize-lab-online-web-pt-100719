class Dog
  
  def initialize(name) 
    @name = name
  end
  
  def name=(name)
    @name = name
  end
  
  def name
    @name
  end
  
  def initialize(dogs_name, dogs_breed="Mutt")
    @name = dogs_name
    @breed = dogs_breed 
  end
  
  def name=(name)
    @name = name
  end
    
  def breed=(breed)
    @breed = breed
  end
    
  def name
    @name
  end
    
  def breed
    @breed
  end
    
end  
  
  